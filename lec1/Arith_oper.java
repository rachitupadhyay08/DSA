public class Arith_oper {
    public static void main(String[] args) {
        int a=8;
        int b=9;
        int c=a+b;
        System.out.println(c);
        c=a-b;
        System.out.println(c);
        c=a*b;
        System.out.println(c);
        c=a/b;
        System.out.println(c);
        c=a%b;
        System.out.println(c);  
    }    
}
